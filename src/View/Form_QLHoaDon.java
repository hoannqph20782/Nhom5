    private void txt_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_TimKiemActionPerformed
        // TODO add your handling code here:String input = txt_TimKiem.getText();
        String input = txt_TimKiem.getText();
        if (input == null) {
            loadTableHoaDon(listhd);
        }
        ArrayList<HoaDonSearch> listHD = new ArrayList<>();
        for (var x : hdService.search(input)) {
            if (x.getMaHoaDon().toLowerCase().contains(input.toLowerCase())
                    || x.getSdtKH().toLowerCase().contains(input.toLowerCase())
                    || x.getMaKH().toLowerCase().contains(input.toLowerCase())) {
                listHD.add(x);
            }
        }
        DefaultTableModel def = (DefaultTableModel) tb_BangHoaDon.getModel();
        def.setRowCount(0);
        for (HoaDonSearch hd : listHD) {
            Object[] data = new Object[]{
                hd.getId(),
                hd.getMaHoaDon(),
                hd.getMaNV(),
                hd.getSdtKH(),
                hd.getNgayTao(),
                hd.getTongTien(),
                hd.getTrangThai() == 1 ? "Đã thanh toán" : "Chưa thanh toán",
                hd.getGhiChu()
            };
            def.addRow(data);
        }
    }//GEN-LAST:event_txt_TimKiemActionPerformed
    public void loadHoaDonChiTiet(String maHoaDon) {
        HoaDonRepo repo = new HoaDonRepo();
        ArrayList<HoaDonChiTiet> listHoaDonChiTiet = repo.getHoaDonChiTietByMaHoaDon(maHoaDon);
        loadTableHoaDonChiTiet(listHoaDonChiTiet);
    }


    private void tb_BangHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_BangHoaDonMouseClicked
        int selectedRow = tb_BangHoaDon.getSelectedRow();
        if (selectedRow >= 2) {
            String maHoaDon = (String) tb_BangHoaDon.getValueAt(selectedRow, 1); // Use column index 1 for MaHoaDon
            System.out.println("Selected MaHoaDon: " + maHoaDon); // Debug statement
            loadHoaDonChiTiet(maHoaDon);
        }
    }//GEN-LAST:event_tb_BangHoaDonMouseClicked

    private void bt_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_SearchActionPerformed
        try {
            if (txt_NgayBD.getDate() == null || txtNgayKT.getDate() == null) {
                loadTableHoaDon(listhd);
                JOptionPane.showMessageDialog(this, "Bắt buộc phải chọn đủ 2 trường ngày bắt đầu và ngày kết thúc!");
            } else {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String dateStart = sdf.format(txt_NgayBD.getDate());
                String dateEnd = sdf.format(txtNgayKT.getDate());
                ArrayList<HoaDonSearch> listKMDate = hdService.searchDate(dateStart, dateEnd);
                DefaultTableModel model = (DefaultTableModel) tb_BangHoaDon.getModel();
                model.setRowCount(0);
                for (HoaDonSearch hd : listKMDate) {
                    Object[] data = new Object[]{
                        hd.getId(),
                        hd.getMaHoaDon(),
                        hd.getMaNV(),
                        hd.getSdtKH(),
                        hd.getNgayTao(),
                        hd.getTongTien(),
                        hd.getTrangThai() == 1 ? "Đã thanh toán" : "Chưa thanh toán",
                        hd.getGhiChu()
                    };
                    model.addRow(data);
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_bt_SearchActionPerformed


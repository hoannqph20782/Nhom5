    public void loadTableHoaDon(ArrayList<HoaDon> hd) {
        model = (DefaultTableModel) tb_BangHoaDon.getModel();
        model.setRowCount(0);
        for (HoaDon hoaDon : hd) {
            Object[] data = new Object[]{
                hoaDon.getId(),
                hoaDon.getMa(),
                hoaDon.getIdNguoiDung().getMaNguoiDung(),
                hoaDon.getIdKhachHang(),
                hoaDon.getNgayTao(),
                hoaDon.getTongTien()
            };
            model.addRow(data);
        }
        listhd = hd;
    }

    public void loadTableHoaDonChiTiet(ArrayList<HoaDonChiTiet> hdc) {
        model = (DefaultTableModel) tb_hoaDonChiTiet.getModel();
        model.setRowCount(0);
        for (HoaDonChiTiet hoaDonChiTiet : hdc) {
            Object[] data = new Object[]{
                hoaDonChiTiet.getIdHD(),
                hoaDonChiTiet.getIdNguoiDung(),
                hoaDonChiTiet.getMaKhachHang(),
                hoaDonChiTiet.getIdKhachHang(),
                hoaDonChiTiet.getNgayTao(),
                hoaDonChiTiet.getIdCTG(),
                hoaDonChiTiet.getSoLuong(),
                hoaDonChiTiet.getDonGia(),};
            model.addRow(data);
        }
        listhdct = hdc;
    }


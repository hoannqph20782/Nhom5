    public void loadTable() {
        ArrayList<KhachHang> kh = this.khRepo.getList();
        DefaultTableModel dfm = (DefaultTableModel) this.tblKhachHang.getModel();
        dfm.setRowCount(0);
        for (KhachHang khm : kh) {
            Object[] row = {
                khm.getMa(),
                khm.getTen(),
                khm.getNgaySinh(),
                khm.getGioiTinh() == 1 ? "Nam" : "Nu",
                khm.getSdt(),
                khm.getEmail(),
                khm.getDiaChi(),
                khm.getTrangThai() == 1 ? "Hoat Dong" : "Khong Hoat Dong",};
            dfm.addRow(row);
        }
    }

    private boolean isValidDate(String dateString) {
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        dateFormat.setLenient(false); // Đảm bảo không chấp nhận ngày không hợp lệ (ví dụ: 31/02/2022)
        try {
            dateFormat.parse(dateString);
            return true;
        } catch (ParseException e) {
            return false;
        }
    }

    public boolean isValidEmail(String email) {
        // Kiểm tra email không được viết hoa
        if (email.matches(".*[A-Z].*")) {
            return false;
        }
        String lowercaseEmail = email.toLowerCase();
        String regex = "^[a-z0-9._%+-]+(\\.[a-z0-9._%+-]+)*@[a-z0-9.-]+\\.[a-z]{2,}$";
        boolean hasConsecutiveDots = lowercaseEmail.contains("..");
        return lowercaseEmail.matches(regex) && !hasConsecutiveDots;
    }

